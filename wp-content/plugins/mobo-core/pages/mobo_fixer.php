        set_transient('mobo_core_remProdDesc_notice', 'No products found with product_guid.', 60);
    }

    wp_reset_postdata();

    // Redirect back (no echo before this)
    $redirect_url = wp_get_referer() ? wp_get_referer() : admin_url();
    wp_redirect($redirect_url);
    exit;
}

function mobo_core_removeDupImage(){
    // Security check
    if (! current_user_can('administrator')) {
        wp_die('You are not allowed to perform this action.');
    }

    global $dup_img_guids;

    $args = [
        'post_type'      => 'attachment',
        'post_status'    => 'any',
        'posts_per_page' => -1, // if huge amount, change to e.g. 200 and rerun
        'fields'         => 'ids',
        'meta_query'     => [
            [
                'key'     => 'img_guid',
                'value'   => $dup_img_guids,
                'compare' => 'IN',
            ],
        ],
    ];

    $query = new WP_Query($args);

    if ( ! $query->have_posts() ) {
        wp_die('No attachments found for the given img_guid values.');
    }

    $deleted = 0;

    foreach ($query->posts as $attachment_id) {
        $result = wp_delete_attachment($attachment_id, true);

        if ($result) {
            $deleted++;
        }
    }

    
    wp_reset_postdata();

    // Redirect back (no echo before this)
    $redirect_url = wp_get_referer() ? wp_get_referer() : admin_url();
    wp_redirect($redirect_url);
    exit;
}


